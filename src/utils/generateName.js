const getRandomInt = (min, max) => Math.floor(Math.random() * (max - min + 1)) + min;

const generateName = () => {
  const idx1 = getRandomInt(0, nameTokens.length - 1);
  const idx2 = getRandomInt(0, nameTokens.length - 1);
  const name1 = nameTokens[idx1];
  const name2 = nameTokens[idx2];
  return `${name1}-${name2}`;
};

